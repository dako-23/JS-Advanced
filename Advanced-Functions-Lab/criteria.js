function solve(dataJson, criteria) {


    const data = JSON.parse(dataJson);

    const criteriaArr = criteria.split('-');
    const [key, value] = criteriaArr



    const result = [];

    data.filter((e) => {
        if (e[key] === value) {
            result.push(e)
        }
    })

    let count = 0

    result.map((e) => {
        console.log(`${count}. ${e.first_name} ${e.last_name} - ${e.email}`);
        count++
    })



}
